# 全部文档中出现的不重复的词的列表
def createVocabList(dataSet):
    vocabSet = set([])
    for document in dataSet:
        vocabSet = vocabSet | set(document)  # \并集
    return list(vocabSet)


# 词集模型
# 输入参数：词汇表、某个文档  输出 文档向量
def setOfWords2Vec(vocabList, inputSet):
    returnVec = [0] * len(vocabList)
    for word in inputSet:
        if word in vocabList:
            returnVec[vocabList.index(word)] = 1
        else:
            print("the word: %s is not in my Vocabulary!" % word)
    return returnVec


# 词袋模型
def bagOfWords2VecMN(vocabList, inputSet):
    returnVec = [0] * len(vocabList)
    for word in inputSet:
        if (word in inputSet):
            returnVec[vocabList.index(word)] + 1
    return returnVec


# 训练数据
# 输入 文档矩阵 trainMatrix,每篇文档类别标签构成的向量trainCategory
def trainNB0(trainMartix, trainCategory):
    numTrainDocs = len(trainMartix)
    numWords = len(trainMartix[0])
    pAbusive = sum(trainCategory) / float(numTrainDocs)  # 侮辱性分类的概率
    p0Num = ones(numWords)
    p1Num = ones(numWords)
    p0Denom = 2.0
    p1Denom = 2.0
    for i in range(numTrainDocs):
        if trainCategory[i] == 1:
            p1Num += trainMartix[i]
            p1Denom += sum(trainMartix[i])  # 单词的总个数
        else:
            p0Num += trainMartix[i]
            p0Denom += sum(trainMartix[i])
    p1Vect = log(p1Num / p1Denom)  # 某个单词数在某个分类中的概率
    p0Vect = log(p0Num / p0Denom)
    return p0Vect, p1Vect, pAbusive


def classifyNB(vec2Classify, p0Vec, p1Vec, pClass1):
    p1 = sum(vec2Classify * p1Vec) + log(pClass1)
    p0 = sum(vec2Classify * p0Vec) + log(1 - pClass1)
    if (p0 > p1):
        return 0
    else:
        return 1


